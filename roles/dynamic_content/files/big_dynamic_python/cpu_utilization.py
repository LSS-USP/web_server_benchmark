import hashlib
import random

MAX_LEN = 1024 * 1024 # Max length to process
MAX_TO_SEND = 80 * 1024 # Max length to send data
HASH_LEN = 57  # Size generated by each hash - 57

def index(req):
    # of hashes
    num_hashes = MAX_LEN / HASH_LEN
    max_to_send = MAX_TO_SEND / HASH_LEN
    data = ""

    for i in range(0, num_hashes):
        temp = hashlib.sha224(str(random.random())).hexdigest()
        if i < max_to_send:
            data += temp

    return(data)
